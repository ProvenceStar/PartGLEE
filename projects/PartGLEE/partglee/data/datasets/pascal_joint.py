def _get_partimagenet_metadata():
    id_to_name = {x['id']: x['name'] for x in PASCAL_JOINT_CATEGORIES}
    thing_dataset_id_to_contiguous_id = {
        x: i for i, x in enumerate(sorted(id_to_name))}
    thing_classes = [id_to_name[k] for k in sorted(id_to_name)]
    return {
        "thing_dataset_id_to_contiguous_id": thing_dataset_id_to_contiguous_id,
        "thing_classes": thing_classes}


def register_pascal_joint_instances(name, metadata, json_file, image_root, dataset_name_in_dict=None):
    DatasetCatalog.register(name, lambda: load_coco_json(
        json_file, image_root, dataset_name=dataset_name_in_dict))
    MetadataCatalog.get(name).set(
        json_file=json_file, image_root=image_root,
        evaluator_type="coco", **metadata
    )

_PASCAL_JOINT = {
    "pascal_joint_train": ("pascal_part/VOCdevkit/VOC2010/JPEGImages", "pascal_part/voc2010_train_joint.json"),
    "pascal_joint_val": ("pascal_part/VOCdevkit/VOC2010/JPEGImages", "pascal_part/voc2010_val_joint.json"),
}

def register_all_pascal_joint(root):
    for key, (image_root, json_file) in _PASCAL_JOINT.items():
        register_coco_instances(
            key,
            _get_partimagenet_metadata(),
            os.path.join(root, json_file) if "://" not in json_file else json_file,
            os.path.join(root, image_root),
            dataset_name_in_dict="pascal_joint",
            evaluator_type='coco'
        )

